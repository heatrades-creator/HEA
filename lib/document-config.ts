// ── Helpers ────────────────────────────────────────────────────────────────────

export function getAnnexBySlug(slug: AnnexSlug): AnnexDef | undefined {
  return ANNEX_REGISTRY.find((a) => a.slug === slug)
}

// Returns the resolved enabled/disabled state for each annex on a document,
// merging saved DB overrides over the registry defaults.
export function resolveDocumentConfig(
  docSlug: DocSlug,
  savedConfig: DocumentConfig
): Record<AnnexSlug, boolean> {
  const docDef = DOCUMENT_REGISTRY.find((d) => d.slug === docSlug)
  if (!docDef) return {} as Record<AnnexSlug, boolean>
  const overrides = savedConfig[docSlug] ?? {}
  const result = {} as Record<AnnexSlug, boolean>
  for (const assignment of docDef.annexes) {
    result[assignment.slug] = overrides[assignment.slug] ?? assignment.defaultEnabled
  }
  return result
}
