// lib/document-config.ts
// Annex and Document type registry — single source of truth for the modular document builder.
// Annex config toggles (per document) are stored in SystemConfig under "document_annex_config".

export type AnnexSlug =
  | 'hea-sa'
  | 'open-solar'
  | 'client-photos-intake'
  | 'client-photos-followup'
  | 'installer-photos'
  | 'financial-outcomes'
  | 'system-spec'
  | 'site-assessment'
  | 'nmi-data'

export type DocSlug =
  | 'job-card'
  | 'electrical-works-proposal'
  | 'wholesale-order'
  | 'work-order'
  | 'completion-report'
  | 'finance-pack'
  | 'grid-connection'

export type AnnexStatus = 'available' | 'planned'

export type AnnexDef = {
  slug: AnnexSlug
  name: string
  description: string
  source: string
  driveSubfolder: string
  filePattern: string
  status: AnnexStatus
}

export type AnnexAssignment = {
  slug: AnnexSlug
  defaultEnabled: boolean
}

export type DocumentDef = {
  slug: DocSlug
  name: string
  description: string
  audience: string
  filePattern: string
  driveSubfolder: string
  annexes: AnnexAssignment[]
}

// Stored per-document config (overrides defaults). Persisted in SystemConfig.
export type DocumentAnnexOverrides = Partial<Record<AnnexSlug, boolean>>
export type DocumentConfig = Partial<Record<DocSlug, DocumentAnnexOverrides>>

